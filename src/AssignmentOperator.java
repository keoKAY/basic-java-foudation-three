public class AssignmentOperator {
    public static void main(String[] args) {
        // Assignment Operators : = , +=, .... %=
        int valueA = 4;
//        int valueB = 2;
//        valueA = valueB; // valueA will be the same as B

        valueA += 2; // valueA = valueA + 2;
        valueA -= 2 ; // valueA = valueA -2 ; // valueA = 6 -2
        System.out.println("Value A is : "+ valueA);
    }
}
